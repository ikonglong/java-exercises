package leetcode;

// 进制转换
public class BaseConversion {
  public static void main(String[] args) {
    int a = 0B01101011;
    System.out.println("a: " + a);
    int b = 0x6B;
    System.out.println("b: " + b);
    System.out.println(Integer.toHexString(107));
    int c = 0xFFFFFFFF;
    System.out.println("c:" + c);
  }
}
